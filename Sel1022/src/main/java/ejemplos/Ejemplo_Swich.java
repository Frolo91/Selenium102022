package ejemplos;

public class Ejemplo_Swich {

	public static void main(String[] args) {
	int i = 6;// variable a validar 
	
	switch(i) {
	
	case 0: // variable evaluada 
		System.out.println("i es cero");
		break;
	case 1:
		System.out.println("i es cero");
		break;
	case 2:
		System.out.println("i es cero");
		break;
	default:
		System.out.println("i es mayor que 2");
		break;
	}
	
	

	}

}
